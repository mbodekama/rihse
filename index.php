                        <h2>Déroulement du protocole HSE</h2>
                        Le management HSE d’une entreprise repose en premier lieu sur un audit exhaustif de la situation et des pratiques en cours, de manière à en évaluer les éventuelles lacunes et à définir et programmer un ensemble d’actions sur le court, le moyen et le long terme.Le protocle se déroule en 04 phase, chaque cycle venant enrichir le précédent :
                        <ul>
                            <li>
                                <h5>Planifier</h5>
                                <p>
                                    A partir de la situation de l’entreprise au regard des problématiques concernées, identifier les objectifs à atteindre et programmer un calendrier de réalisation.
                                </p>
                            </li>
                            <li>
                                <h5>Réaliser</h5>
                                <p>
                                    Mettre en œuvre le processus de management HSE conformément au plan établi.
                                </p>
                            </li>
                            <li>
                                <h5>Évaluer </h5>
                                <p>
                                    Analyser les résultats obtenus ou non grâce à un tableau d’indicateurs pertinents.
                                </p>
                            </li>
                            <li>
                                <h5>Ancrer/améliorer </h5>
                                <p>
                                    Valider et consolider les acquis, ajuster et corriger les stratégies pour l’obtention complète des résultats en cours d’acquisition, établir des objectifs supplémentaires en fonction de la nouvelle situation.
                                </p>
                            </li>
